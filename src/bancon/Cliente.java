    private void SGActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SGActionPerformed
Ventana1 N=new Ventana1();
         String nuevo,buscar =NoC.getText();  
         
        try {
       p=conx.getConnection().prepareStatement("SELECT * FROM clientes WHERE NoCuenta=?");
            p.setString(1,buscar);
            BP=p.executeQuery();
            N.setNoCuenta(buscar);
            
            if(BP.next()){
                nuevo=BP.getString("contraseña");
                
                if(contra.getText().equals(nuevo)){
                Ventana1 abrir=new Ventana1();
                abrir.setVisible(true);
                Ventana1.txt1.setText(buscar);
                this.setVisible(false);
            
               }else{
                    JOptionPane.showMessageDialog(null,"Contraseña incorrecta");
                }
             
            }else{
            JOptionPane.showMessageDialog(null,"Usuario incorrecto");
            }
             BP.close();
    
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e, "Error durante el procedimiento", JOptionPane.ERROR_MESSAGE);

    }
       
    }//GEN-LAST:event_SGActionPerformed

