    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int DINERO,RETIRO;
                float T,M,C,D;
             CuentasClientes ubdate = new CuentasClientes();
                
      try {
       p=conx.getConnection().prepareStatement("SELECT * FROM clientes WHERE NoCuenta=?");
            p.setString(1,TXTC.getText());
            BP=p.executeQuery();
            if(BP.next()){
               String  nuevo=BP.getString("contraseña");
                if(TXTCONTRA.getText().equals(nuevo)){
                 DINERO=Integer.parseInt(BP.getString("dinero"));
                 M=Float.parseFloat(BP.getString("multa"));
                 D=Float.parseFloat(BP.getString("Dretirado"));
                 RETIRO=Integer.parseInt(TXTRT.getText());
                 T=DINERO/2;  
                 if(RETIRO<=DINERO){
                     C=DINERO-RETIRO;
                     D=D+RETIRO;
                   if(RETIRO>T){
                     M=M+100;
                   }                 
                  ubdate.Retiro(TXTC.getText(), String.valueOf(C), String.valueOf(M), String.valueOf(D));
                  SALIDA.setText("RETIRO EXITOSO!"); 
                 } else{
                 SALIDA.setText("DINERO INSUFICIENTE");
                 }  
                    
               
                    
               }else{
                    JOptionPane.showMessageDialog(null,"Contraseña incorrecta");
                }
             
            }else{
            JOptionPane.showMessageDialog(null,"Usuario incorrecto");
            }
             BP.close();
    
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e, "Error durante el procedimiento", JOptionPane.ERROR_MESSAGE);

    }                    
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

